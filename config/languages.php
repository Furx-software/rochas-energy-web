// Cache global para las traducciones
$translations_cache = [];

/**
 * Obtener el idioma actual
 */
function getCurrentLanguage() {
    // Si hay un parámetro GET para cambiar idioma
    if (isset($_GET['lang']) && array_key_exists($_GET['lang'], ['es' => true, 'en' => true, 'ca' => true, 'pt' => true, 'fr' => true])) {
        $_SESSION['lang'] = $_GET['lang'];
    }
    
    // Retornar el idioma de la sesión o el predeterminado
    return isset($_SESSION['lang']) ? $_SESSION['lang'] : 'es';
}

/**
 * Obtener todas las traducciones para un idioma
 */
function getTranslations($language) {
    global $translations, $translations_cache;
    
    // Verificar cache
    if (isset($translations_cache[$language])) {
        return $translations_cache[$language];
    }
    
    // Obtener traducciones
    $result = isset($translations[$language]) ? $translations[$language] : $translations['es'];
    
    // Guardar en cache
    $translations_cache[$language] = $result;
    
    return $result;
}

/**
 * Función de traducción optimizada
 */
function t($key, $section = 'nav') {
    static $current_translations = null;
    static $current_language = null;
    
    // Obtener idioma actual
    $lang = getCurrentLanguage();
    
    // Si cambió el idioma, actualizar cache
    if ($current_language !== $lang) {
        $current_language = $lang;
        $current_translations = getTranslations($lang);
    }
    
    // Si no tenemos traducciones, obtenerlas
    if ($current_translations === null) {
        $current_translations = getTranslations($lang);
    }
    
    // Buscar la traducción
    if (isset($current_translations[$section][$key])) {
        return $current_translations[$section][$key];
    }
    
    // Si no se encuentra, retornar la clave
    return $key;
}

/**
 * Limpiar cache de traducciones
 */
function clearTranslationsCache() {
    global $translations_cache;
    $translations_cache = [];
}
