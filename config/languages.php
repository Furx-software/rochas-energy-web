 * FunciÃ³n de traducciÃ³n optimizada
 */
function t($key, $section = 'nav') {
    static $current_translations = null;
    static $current_language = null;
    
    // Obtener idioma actual
    $lang = getCurrentLanguage();
    
    // Si cambiÃ³ el idioma, actualizar cache
    if ($current_language !== $lang) {
        $current_language = $lang;
        $current_translations = getTranslations($lang);
    }
    
    // Si no tenemos traducciones, obtenerlas
    if ($current_translations === null) {
        $current_translations = getTranslations($lang);
    }
    
    // Buscar la traducciÃ³n
    if (isset($current_translations[$section][$key])) {
        return $current_translations[$section][$key];
    }
    
    // Si no se encuentra, retornar la clave
    return $key;
}

